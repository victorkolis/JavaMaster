package java_basics;

public class Variables {
	
	public static void main(String[] args) {
		int x; /* Declaring */
		x = 127; /* Assigning */
		System.out.println(x);
		
		
		x = 721;
		System.out.println(x);
		
		
		String quote = "To be or not to be[...]";
		System.out.println(quote);
		System.out.println(1 + 2);
		
	}

}
