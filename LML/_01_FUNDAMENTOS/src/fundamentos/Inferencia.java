package fundamentos;

public class Inferencia {

	public static void main(String[] args) {
		
		var age = 32;  /* var has no predefined type */
		
		System.out.println(age);
		
		/* Primitive */
		int b = 0;
		long a = 1;
		short c = 2;
		byte d = 127;
		char e = '\u0010';
		
		/* Object/Class */
		String name = "Victor Kolis";
		
		System.out.println(a + b + c + d + e);
		System.out.println(name);
		
		
	}
}
